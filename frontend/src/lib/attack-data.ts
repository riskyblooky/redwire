function t(id: string, name: string, tacticIds: string[], parentId?: string): AttackTechnique {
    const isSub = id.includes('.');
    const urlPath = isSub ? id.replace('.', '/') : id;
    return {
        id,
        name,
        tacticIds,
        isSubtechnique: isSub,
        parentId: parentId || (isSub ? id.split('.')[0] : undefined),
        url: `https://attack.mitre.org/techniques/${urlPath}`,
    };
}

