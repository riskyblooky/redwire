// ── Lookup Maps & Helpers ─────────────────────────────────────────────

export const TECHNIQUE_MAP = new Map<string, AttackTechnique>(
    TECHNIQUES.map(t => [t.id, t])
);

export const TACTIC_MAP = new Map<string, AttackTactic>(
    TACTICS.map(t => [t.id, t])
);

/** Get all techniques (base + sub) for a given tactic. */
export function getTechniquesByTactic(tacticId: string): AttackTechnique[] {
    return TECHNIQUES.filter(t => t.tacticIds.includes(tacticId));
}

/** Get only BASE techniques for a tactic (no sub-techniques). */
export function getBaseTechniquesByTactic(tacticId: string): AttackTechnique[] {
    return TECHNIQUES.filter(t => t.tacticIds.includes(tacticId) && !t.isSubtechnique);
}

/** Get sub-techniques of a parent technique. */
export function getSubTechniques(parentId: string): AttackTechnique[] {
    return TECHNIQUES.filter(t => t.parentId === parentId && t.isSubtechnique);
}

/** Resolve a technique ID to its display label: "T1059.001 — PowerShell" */
export function getTechniqueLabel(id: string): string {
    const tech = TECHNIQUE_MAP.get(id);
    return tech ? `${tech.id} — ${tech.name}` : id;
}

/** Resolve a technique ID to just its name. */
export function getTechniqueName(id: string): string {
    return TECHNIQUE_MAP.get(id)?.name ?? id;
}

/** Search techniques by ID or name substring (case-insensitive). */
export function searchTechniques(query: string): AttackTechnique[] {
    const q = query.toLowerCase().trim();
    if (!q) return [];
    return TECHNIQUES.filter(
        t => t.id.toLowerCase().includes(q) || t.name.toLowerCase().includes(q)
    );
}

